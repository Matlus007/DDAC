        private readonly UserManager<Week3_MVCFlowerShopUser> _userManager;
        private readonly IUserStore<Week3_MVCFlowerShopUser> _userStore;
        private readonly IUserEmailStore<Week3_MVCFlowerShopUser> _emailStore;
        private readonly ILogger<RegisterModel> _logger;
        private readonly IEmailSender _emailSender;

        public RegisterModel(
            UserManager<Week3_MVCFlowerShopUser> userManager,
            IUserStore<Week3_MVCFlowerShopUser> userStore,
            SignInManager<Week3_MVCFlowerShopUser> signInManager,
            ILogger<RegisterModel> logger,
            IEmailSender emailSender)
        {
            _userManager = userManager;
            _userStore = userStore;
            _emailStore = GetEmailStore();
            _signInManager = signInManager;
            _logger = logger;
            _emailSender = emailSender;
        }

        public SelectList RoleSelectList = new SelectList(
            new List<SelectListItem>
        {
        new SelectListItem { Selected =true, Text = "Select Role", Value = ""},
        new SelectListItem { Selected =true, Text = "Admin", Value = "Admin"},
        new SelectListItem { Selected =true, Text = "Customer", Value = "Customer"},
        }, "Value", "Text", 1);

